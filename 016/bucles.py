# f strings
for i in range(5):      # 0, 1, 2, 3, 4
    print(f"El valor de la variable es {i}")


for i in range(5,10):      # desde el 5, al 9 ---> 5,6,7,8,9
    print(f"El valor de la variable es {i}")


for i in range(5,34,2):      # desde el 5, al 33, saltando de a 2
    print(f"El valor de la variable es {i}")
